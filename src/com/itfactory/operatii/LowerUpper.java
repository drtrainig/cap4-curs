public class LowerUpper {
    public static void main(String[] args) {

        String mesaj = "asdanfjandoianoskidnOAOJNOJNAOSNdkonokmoasASSD";
        String mesajLowerCase = mesaj.toLowerCase();
        String mesajUpperCase = mesaj.toUpperCase();

        System.out.println(mesajLowerCase);
        System.out.println(mesajUpperCase);
    }
    
}
